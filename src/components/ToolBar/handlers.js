export const handleContenido = (
  event,
  bodyFiltros,
  setBodyFiltros,
  setPage
) => {
  setPage(1);
  let desde, hasta;
  switch (parseInt(event.target.value)) {
    case 400:
      desde = 0;
      hasta = 400;
      break;
    case 500:
      desde = 401;
      hasta = 500;
      break;
    case 700:
      desde = 501;
      hasta = 700;
      break;
    case 1000:
      desde = 701;
      hasta = 1000;
      break;
    default:
      desde = 0;
      hasta = 0;
      break;
  }
  setBodyFiltros({
    ...bodyFiltros,
    contenido: { ...bodyFiltros.contenido, desde: desde, hasta: hasta },
  });
};

